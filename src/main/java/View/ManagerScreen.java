    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        File f = p.getSelectedFile();
        String in = f.getAbsolutePath();
        Path path = (Path) Paths.get(in);
        c.addmovie(mname.getText(), desc.getText().replaceAll(",", ";"), Double.parseDouble(mprice.getText()),
                new File("" + c.storeImg(path, f.getName())));
        dms.removeAllItems();
        smovie.removeAllItems();
        ArrayList<Movie> a = c.getallMovie();
        for (Movie x : a) {
            smovie.addItem("" + c.getMoviename(x));
            dms.addItem("" + c.getMoviename(x));
        }
        mname.setText("");
        desc.setText("");
        mprice.setText("");
        ou.setIcon(null);
    }//GEN-LAST:event_jButton7ActionPerformed

