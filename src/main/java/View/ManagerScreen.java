    private void atActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atActionPerformed
        thb++;
        if (thb <= max) {

            c.addtheater();
            dts.removeAllItems();
            th.removeAllItems();
            for (Theater x : c.getallTheater()) {
                th.addItem("" + c.getTheaterID(x));
                dts.addItem("" + c.getTheaterID(x));
            }
        }
        if (thb == max) {
            at.setVisible(false);
        }
    }//GEN-LAST:event_atActionPerformed
   
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(c.getScreentheaters().containsKey(th.getSelectedItem().toString()))JOptionPane.showConfirmDialog(this, "This Theater already has a movie","Info",JOptionPane.YES_OPTION,JOptionPane.ERROR_MESSAGE);
        else if(c.getScreentheaters().containsValue(smovie.getSelectedItem().toString()))JOptionPane.showConfirmDialog(this, "This Movie is already in a Theater","Info",JOptionPane.YES_OPTION,JOptionPane.ERROR_MESSAGE);
        else if (c.getScreennb()!=max){
        char chosen = th.getSelectedItem().toString().toCharArray()[0];
        c.editTheaterTime(chosen, time.getText());
        c.addScreen(chosen, smovie.getSelectedItem().toString());
        time.setText("");}else{
            jButton1.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void dtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dtActionPerformed
        thb--;
        if (thb - 1 < max) {
            at.setVisible(true);
        }

        c.delete(c.getTheater(dts.getSelectedItem().toString().toCharArray()[0]));
        dts.removeAllItems();
        th.removeAllItems();
        for (char x : c.getallTheaterID()) {
            th.addItem("" + x);
            dts.addItem("" + x);
        }
        if(c.getScreennb()<6){
            jButton1.setVisible(true);
        }
    }//GEN-LAST:event_dtActionPerformed

